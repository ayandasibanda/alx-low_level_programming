/**
 * struct dog - a type representing a dog with a name, age, and owner
 * @name: a pointer to a character string representing the name of the dog
 * @age: a float representing the age of the dog
 * @owner: a pointer to a character string representing the name of the dog's owner
 *
 * Description: This type represents a dog with a name, age, and owner.
 */
struct dog {
    char *name;
    float age;
    char *owner;
};

